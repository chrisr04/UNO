    private void numeroJugadoresItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_numeroJugadoresItemStateChanged
        
        if(numeroJugadores.getSelectedItem()=="2")
        {
            jugador1Txt.setEnabled(true);
            jugador2Txt.setEnabled(true);
            jugador3Txt.setEnabled(false);
            jugador4Txt.setEnabled(false);
        } 
        else if(numeroJugadores.getSelectedItem()=="3")
        {
            jugador1Txt.setEnabled(true);
            jugador2Txt.setEnabled(true);
            jugador3Txt.setEnabled(true);
            jugador4Txt.setEnabled(false);
        }
        else if(numeroJugadores.getSelectedItem()=="4")
        {
            jugador1Txt.setEnabled(true);
            jugador2Txt.setEnabled(true);
            jugador3Txt.setEnabled(true);
            jugador4Txt.setEnabled(true);
        }
    }//GEN-LAST:event_numeroJugadoresItemStateChanged

    private void botonJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonJugarActionPerformed
        UnoInterfazGrafica abrir = new UnoInterfazGrafica();
        
        
        if(numeroJugadores.getSelectedItem()=="2")
        { 
            if(jugador1Txt.getText().equals("") || jugador2Txt.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Ingrese los nombres de los jugadores seleccionados"); 
            }
            else
            {   
                nickname.add(jugador1Txt.getText());
                nickname.add(jugador2Txt.getText());
                if(inicio.iniciarSesion(nickname))
                {
                    abrir.implementacion = inicio;
                    abrir.setVisible(true);
                    this.setVisible(false);                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Los nombres de usuario no pueden ser repetidos");
                    nickname.clear();
                }
            }
        } 
        else if(numeroJugadores.getSelectedItem()=="3")
        {
            if(jugador1Txt.getText().equals("") || jugador2Txt.getText().equals("")|| jugador3Txt.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Ingrese los nombres de los jugadores seleccionados");
            }
            else
            {
                nickname.add(jugador1Txt.getText());
                nickname.add(jugador2Txt.getText());
                nickname.add(jugador3Txt.getText());
                if(inicio.iniciarSesion(nickname))
                {
                    abrir.implementacion = inicio;
                    abrir.setVisible(true);
                    this.setVisible(false);                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Los nombres de usuario no pueden ser repetidos");
                    nickname.clear();
                }
            }
        }
        else if(numeroJugadores.getSelectedItem()=="4")
        {
           if(jugador1Txt.getText().equals("") || jugador2Txt.getText().equals("") || jugador3Txt.getText().equals("") || jugador4Txt.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Ingrese los nombres de los jugadores seleccionados");
            }
           else
           {
                nickname.add(jugador1Txt.getText());
                nickname.add(jugador2Txt.getText());
                nickname.add(jugador3Txt.getText());
                nickname.add(jugador4Txt.getText());
                if(inicio.iniciarSesion(nickname))
                {
                    abrir.implementacion = inicio;
                    abrir.setVisible(true);
                    this.setVisible(false);                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Los nombres de usuario no pueden ser repetidos");
                    nickname.clear();
                }

           }
        }
    }//GEN-LAST:event_botonJugarActionPerformed

